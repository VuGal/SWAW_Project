/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_TIM1_Init();
	MX_I2C1_Init();
	MX_I2C2_Init();
	MX_CAN_Init();

	/* USER CODE BEGIN 2 */

	// Timer start
	HAL_TIM_Base_Start(&htim1);

	// CAN filter parameters
	sFilterConfig.FilterBank = 0;
	sFilterConfig.FilterMode = CAN_FILTERMODE_IDMASK;
	sFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;
	sFilterConfig.FilterIdHigh = 0x0000;
	sFilterConfig.FilterIdLow = 0x0000;
	sFilterConfig.FilterMaskIdHigh = 0x0000;
	sFilterConfig.FilterMaskIdLow = 0x0000;
	sFilterConfig.FilterFIFOAssignment = CAN_RX_FIFO0;
	sFilterConfig.FilterActivation = ENABLE;
	sFilterConfig.SlaveStartFilterBank = 14;

	// CAN TX header parameters
	TxHeader.StdId = 0x321;
	TxHeader.ExtId = 0x01;
	TxHeader.RTR = CAN_RTR_DATA;
	TxHeader.IDE = CAN_ID_STD;
	TxHeader.DLC = 8;
	TxHeader.TransmitGlobalTime = DISABLE;

	// apply CAN filter configuration
	if (HAL_CAN_ConfigFilter(&hcan, &sFilterConfig) != HAL_OK)
	{
	  /* Filter configuration Error */
	  Error_Handler();
	}

	// start CAN bus operation
	if (HAL_CAN_Start(&hcan) != HAL_OK)
	{
	  /* Start Error */
	  Error_Handler();
	}

	// BMP280 parameters
	bmp280_init_default_params(&bmp280.params);
	bmp280.addr = BMP280_I2C_ADDRESS_0;
	bmp280.i2c = &hi2c1;

	// BMP280 initialization
	init_iter = 0;
	while (1) {

		if (bmp280_init(&bmp280, &bmp280.params)) break;
		else {
			HAL_Delay(1000);
			++init_iter;
		}

		if (init_iter > 100) Error_Handler();

	}

	// BH1750 initialization
	init_iter = 0;
	while (1) {

		if (BH1750_Init(&hi2c2) == BH1750_OK) break;
		else {
			HAL_Delay(1000);
			++init_iter;
		}

		if (init_iter > 100) Error_Handler();

	}
	BH1750_SetMode(CONTINUOUS_HIGH_RES_MODE_2);

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)
	{
	/* USER CODE END WHILE */

	/* USER CODE BEGIN 3 */

	// DHT22 read-out
	DHT22_Start();
	Presence = DHT22_Check_Response();
	Rh_byte1 = DHT22_Read();
	Rh_byte2 = DHT22_Read();
	Temp_byte1 = DHT22_Read();
	Temp_byte2 = DHT22_Read();
	DHT22_Read();

	// bit operations needed for 2-byte representation
	TEMP = ((Temp_byte1<<8)|Temp_byte2);
	RH = ((Rh_byte1<<8)|Rh_byte2);

	// divide by 10
	Temperature = TEMP / 10;
	Humidity = RH / 10;

	// it seems that initial values of BMP280 are more accurate when this delay takes place
	HAL_Delay(1000);

	// BMP280 read-out
	init_iter = 0;
	while (1) {

		if (bmp280_read_float(&bmp280, &temperatureBmp280, &pressureBmp280, &humidityBmp280)) break;
		else {
			HAL_Delay(1000);
			++init_iter;
		}

		if (init_iter > 100) Error_Handler();

	}

	pressureBmp280 /= 100;

	// it seems that initial values of BH1750 are more accurate when this delay takes place
	HAL_Delay(1000);

	// BH1750 read-out
	BH1750_ReadLight(&BH1750_lux);

	// Data array filling
	TxData[0] = Temperature;
	TxData[1] = Humidity;
	TxData[2] = pressureBmp280;
	TxData[3] = BH1750_lux;

	// Send message on CAN TX
	HAL_CAN_AddTxMessage(&hcan, &TxHeader, (uint8_t *) TxData, &TxMailbox);

	// Get message from CAN RX
	HAL_CAN_GetRxMessage(&hcan, CAN_RX_FIFO0, &RxHeader, (uint8_t *) RxData);

	// 30 minutes delay
	HAL_Delay(180000);

  }
  /* USER CODE END 3 */
}

